#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	tmp = ft_lstlast(*lst);
	if (tmp == NULL)
		ft_lstadd_front(&(*lst), new);
	else
		tmp->next = new;
}
